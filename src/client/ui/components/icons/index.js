/**
 * When using an icon, We always want to preserve
 * the aspect ratio. But we want to be able to specify
 * an exact width _or_ height rather than force user to
 * set a desired height via an icon's width
 */
const preserveRatio = (ratio) => ({ w, h, defaultSize }) => {
  if (w) return { width: w, height: w * ratio }
  if (h) return { width: h * (ratio + 1), height: h }

  return {
    width: defaultSize,
    height: defaultSize * ratio
  }
}

/**
 * @param color
 * @param w - desired icon width
 * @param h - desired icon height
 * This wrapper takes on the task of determining icon
 * dimensions from an individual icon's configuration +
 * user input
 *
 * set exact width. height will be set based on aspect ratio
 * <Icon w={50} />
 *
 * set exact height. width will be set based on aspect ratio
 * <Icon h={20} />
 *
 * Will use "blue" color from theme context
 * <Icon color='blue' />
 */
